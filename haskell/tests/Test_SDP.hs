{-|
Module      : Test_SDP
Description : This module contains property-based tests for the SDP (Stochastic Dynamic Programming) framework.
              It uses QuickCheck to verify the correctness of various properties related to policies, policy sequences,
              and value functions.

Key Components:
- `Action` and `State`: Data types representing possible actions and states in the SDP model.
- Arbitrary Instances: Instances of `Arbitrary` for `Action` and `State` to enable random generation of test cases.
- `dummyProb2`: A helper function to create a normalized probability distribution over a list of elements.
- `dpol`, `dpol2`, ..., `dpol6`: Example policies mapping states to actions.
- `dpolseq`: A sequence of example policies.
- `testSDP`: A sample SDP instance with predefined reward, transition, action, and state functions.
- `genPolicySeq`: A generator for creating random policy sequences for a given timestep.

Properties Tested:
1. `prop_valEmptyPolicy`: Verifies that the value function returns 0 when the policy sequence is empty.
2. `prop_biLength`: Ensures that the length of the policy sequence generated by `bi` matches the specified horizon.
3. `prop_valNonNegative`: Confirms that the value function always returns a non-negative value.
4. `prop_bi_empty_policy`: Checks that `bi` returns an empty policy sequence when the horizon (`n`) is less than or equal to 0.

Main Function:
- Runs QuickCheck tests for the defined properties to validate the behavior of the SDP framework.
-}
module Test_SDP where

import Test.QuickCheck
import SDPCompute
import Prob
import Data.Map (Map)
import qualified Data.Map as Map

data Action = Start | Delay deriving (Show, Eq, Enum, Ord, Read)
data State  = S1 | S2 | S3 deriving (Show, Eq, Enum, Ord, Read)

instance Arbitrary Action where
    arbitrary = elements [Start, Delay]

instance Arbitrary State where 
    arbitrary = elements [S1, S2, S3]

dummyProb2 :: [a] -> Prob a
dummyProb2 xs = normalize (Prob [(x, 1.0) | x <- xs])

testSDP :: SDP State Action
testSDP = SDP {
    reward  = \_ _ _ _ -> 1.0,
    next    = \_ _ _   -> dummyProb2 [S1, S2, S3],
    actions = \_ s -> case s of
        S1 -> [Start, Delay]
        S2 -> [Start, Delay]
        S3 -> [Start, Delay],
    states  = \_       -> [S1, S2, S3]
}

-- =====================================================================
-- Test val
-- =====================================================================
{-
    - Verifies that when the policy sequence is empty, the value function `val` returns 0.
    - Preconditions: The time `t` must be non-negative.
-}
prop_valEmptyPolicy :: Int -> State -> Property
prop_valEmptyPolicy t x =
    t  >= 0 ==> val testSDP t [] x == 0

{-
    - Ensures that the length of the policy sequence generated by `bi` matches the specified horizon `n`.
    - Preconditions: The horizon `n` must be non-negative.
-}
prop_biLength :: Int -> Int -> Property
prop_biLength t n = (n >= 0) ==> length (bi testSDP t n) == n

{-
    - Checks that the value function `val` always returns a non-negative value for any generated policy sequence.
    - Preconditions: Random values for `t` (between 0 and 8) and `n` (between 1 and 8) are used to generate the policy sequence.
-}
prop_valNonNegative :: Int ->  State -> Property
prop_valNonNegative t x =
    forAll (choose (0, 8)) $ \t ->  -- Generate t between 0 and 8
    forAll (choose (1, 8)) $ \n ->  -- Generate n between 1 and 10
    let policySeq = bi testSDP t n  
    in val testSDP t policySeq x >= 0

{-
    - Tests that extending a policy sequence by repeating its last element does not decrease the value returned by `val`.
    - Preconditions: Random values for `t` (between 0 and 8) and `n` (between 1 and 8) are used to generate the policy sequence.
    - Ensures that a longer policy sequence is at least as good as the original sequence.
-}
prop_longerPolicyBetterOrEqual :: Int ->  State -> Property
prop_longerPolicyBetterOrEqual t x =
    forAll (choose (0, 8)) $ \t ->  -- Generate t between 0 and 8
    forAll (choose (1, 8)) $ \n ->  -- Generate n between 1 and 10 
    let ps = bi testSDP t n
    in
        not (null ps) ==> 
        let v1 = val testSDP t ps x
            v2 = val testSDP t (ps ++ [last ps]) x
        in v2 >= v1

-- =====================================================================
-- Test bi
-- =====================================================================

-- Property: bi should return an empty policy sequence when n <= 0, regardless timestep (value of t)
prop_bi_empty_policy ::  Int -> Property
prop_bi_empty_policy t = 
    forAll (choose (0, 8)) $ \t ->
    let ps = bi testSDP t 0
    in ps == []

-- The sequence produced should have exactly n elements.
prop_bi_correct_length :: Int -> Property
prop_bi_correct_length t =
    forAll(choose (0, 8)) $ \t ->
    forAll(choose (1, 8)) $ \n ->
    let ps = bi testSDP t n
    in length(ps) == n

{-
Every element of the sequence should be a valid Policy, meaning:
    It defines an action for every reachable state at that time t.
-}

prop_bi_correct_elements :: Property
prop_bi_correct_elements =
  forAll (choose (0, 8)) $ \t ->
  forAll (choose (1, 8)) $ \n ->
    let ps = bi testSDP t n
        allStates = states testSDP t  -- FIX: call states correctly
    in all (\p -> all (`Map.member` p) allStates) ps



----------------------------------------------------------------------

testVal :: IO()
testVal = do
    quickCheck prop_biLength
    quickCheck prop_valEmptyPolicy
    quickCheck prop_valNonNegative
    quickCheck prop_longerPolicyBetterOrEqual

testBi :: IO()
testBi = do
    quickCheck prop_bi_empty_policy
    quickCheck prop_bi_correct_length
    quickCheck prop_bi_correct_elements 

testAll :: IO ()
testAll = do
    testVal



runBi :: Int -> Int -> PolicySeq State Action
runBi t n = bi testSDP t n